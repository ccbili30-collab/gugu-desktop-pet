def _ground_row(frame: tuple[str, ...]) -> int:
    leg_rows = [row_index for row_index, row in enumerate(frame) if "L" in row]
    if leg_rows:
        return max(leg_rows)
    filled_rows = [row_index for row_index, row in enumerate(frame) if any(pixel != TRANSPARENT for pixel in row)]
    return max(filled_rows) if filled_rows else 0


