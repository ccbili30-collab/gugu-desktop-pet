"""Independent pixel frames for the desktop pigeon."""

from __future__ import annotations

WIDTH = 16
HEIGHT = 16
TRANSPARENT = "."


def _frame(*lines: str) -> tuple[str, ...]:
    if len(lines) != HEIGHT:
        raise ValueError(f"expected {HEIGHT} rows, got {len(lines)}")
    for line in lines:
        if len(line) != WIDTH:
            raise ValueError(f"expected row width {WIDTH}, got {len(line)}: {line!r}")
    return tuple(lines)


